public class ifElse {
    public static void main(String[] args){
        int age = 18;
        if(age>18){
            System.out.println("age is above 18");
        }
        else if (age==18) {
            System.out.println("Age is equal to 18");
        }
        else{
            System.out.println("age is below 18");
        }
    }
}